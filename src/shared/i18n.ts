export function t(lang: UiLanguage, key: string, vars?: Vars): string {
  const template = (lang === "cs" ? cs[key] : en[key]) ?? key;
  if (!vars) {
    return template;
  }
  return template.replace(/\{([A-Za-z0-9_]+)\}/g, (_m, name) => {
    const v = vars[name];
    return v === undefined ? `{${name}}` : String(v);
  });
}
